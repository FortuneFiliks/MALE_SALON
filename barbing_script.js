let slide_Index = 1;
show_Slide(slide_Index);

function plus_Slides(n){
	show_Slide(slide_Index += n);
}


function show_Slide(n){
	let get_Image_Container = document.getElementsByClassName("barbing_images_slide");
	let i;
	if(n > get_Image_Container.length){
		slide_Index = 1;
	}
	if(n < 1){
		slide_Index = get_Image_Container.length;
	}	
	for(i = 0; i < get_Image_Container.length; i++){
		get_Image_Container[i].style.display = "none";
	}
	get_Image_Container[slide_Index - 1].style.display = "inline";
}